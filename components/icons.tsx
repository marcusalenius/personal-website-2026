// Phosphor renders every glyph inside a padded 256-unit box, and the padding
// differs per icon — so a uniform `size` makes the visible ink look unequal and
// leaves invisible space between an icon and adjacent text. These wrappers inline
// the (regular-weight) path with the viewBox cropped exactly to the path bounds,
// so `size` maps 1:1 to visible ink and there is no padding to align around.
// viewBoxes were measured from the path data; see the bold ArrowUpRight inlined
// in MDLink for the same technique applied to inline link glyphs.

type IconProps = { size?: number; className?: string };

function croppedIcon(path: string, viewBox: string) {
  const [, , w, h] = viewBox.split(" ").map(Number) as [
    number,
    number,
    number,
    number,
  ];
  const ratio = w / h;
  return function CroppedIcon({ size = 16, className }: IconProps) {
    // `size` sets the larger dimension; the other scales to keep aspect ratio.
    const width = ratio >= 1 ? size : size * ratio;
    const height = ratio >= 1 ? size / ratio : size;
    return (
      <svg
        viewBox={viewBox}
        width={width}
        height={height}
        fill="currentColor"
        className={className}
        aria-hidden
      >
        <path d={path} />
      </svg>
    );
  };
}

